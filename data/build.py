def to_simplified(text: str) -> str:
    if text is None:
        return ""

    text = str(text)
    if OPENCC_CONVERT is not None:
        text = OPENCC_CONVERT(text)

    return text.translate(COMMON_T2S)


def clean_line(line: str) -> str:
    line = to_simplified(line).strip()
    line = re.sub(r"[（）()《》「」『』\[\]]", "", line)
    line = re.sub(r"\s+", "", line)
    return line

def infer_style(text):
    style_rules = [
        ("豪放", ["边塞", "黄沙", "战马", "孤城", "胡马", "旌旗", "剑", "长歌"]),
        ("婉约", ["春", "花", "柳", "雨", "江南", "桃花", "流水"]),
        ("含蓄", ["月", "乡", "故人", "梦", "归", "客", "思"]),
        ("清冷", ["雪", "霜", "寒", "孤", "夜", "灯", "秋"]),
        ("伤感", ["离", "别", "泪", "愁", "断肠", "长亭"]),
        ("自然", ["山", "水", "云", "竹", "梅", "松", "泉"]),
    ]

    scores = {}
    for style, words in style_rules:
        scores[style] = sum(1 for w in words if w in text)

    best_style = max(scores, key=scores.get)

    if scores[best_style] == 0:
        return "典雅"

    return best_style
def get_source_type(file_path: Path):
    file_str = str(file_path)
    if "ci.song" in file_str or "宋词" in file_str:
        return "宋词"
    if "poet.tang" in file_str or "全唐诗" in file_str:
        return "唐诗"
    return "古诗词"

def split_poem_lines(paragraphs):
    """
    把 chinese-poetry 里的 paragraphs 切成真正的诗句。
    例如：
    ["聞道黃花戍，頻年不解兵。", "可憐閨裏月，偏照漢家營。"]
    切成：
    ["聞道黃花戍", "頻年不解兵", "可憐閨裏月", "偏照漢家營"]
    """
    lines = []

    for p in paragraphs:
        p = to_simplified(p).strip()
        p = re.sub(r"[（）()《》「」『』\[\]]", "", p)
        parts = re.split(r"[，。！？；、\s]+", p)

        for part in parts:
            part = part.strip()
            if part:
                lines.append(part)

    return lines
def detect_form(lines, source_type):
    """
    判断五言绝句 / 七言绝句。
    lines 必须已经是切好的单句。
    """
    clean_lines = [re.sub(r"[，。！？、；\s]", "", x) for x in lines]
    lens = [len(x) for x in clean_lines if x]

    if source_type == "唐诗":
        if len(clean_lines) == 4 and all(x == 5 for x in lens):
            return "五言绝句"

        if len(clean_lines) == 4 and all(x == 7 for x in lens):
            return "七言绝句"

        # 如果你只想训练五言/七言绝句，这里建议 return None
        return None

    if source_type == "宋词":
        if 2 <= len(clean_lines) <= 20:
            return "宋词"
        return None

    return None

def extract_keywords(text):
    text = to_simplified(text)
    hits = []

    preferred_keywords = sorted(dict.fromkeys(PREFERRED_KEYWORDS), key=lambda x: (-len(x), PREFERRED_KEYWORDS.index(x)))
    fallback_keywords = sorted(dict.fromkeys(FALLBACK_KEYWORDS), key=lambda x: (-len(x), FALLBACK_KEYWORDS.index(x)))

    for kw in preferred_keywords:
        if kw in text and kw not in hits:
            hits.append(kw)

    if len(hits) < 3:
        for kw in fallback_keywords:
            if kw in text and kw not in hits:
                hits.append(kw)
            if len(hits) >= 3:
                break

    if not hits:
        return None

    concrete_hits = [kw for kw in hits if len(kw) >= 2]
    fallback_hits = [kw for kw in hits if len(kw) < 2]
    random.shuffle(concrete_hits)
    random.shuffle(fallback_hits)
    return (concrete_hits + fallback_hits)[:3]


def normalize_for_keyword(text):
    return re.sub(r"[^\u4e00-\u9fff]", "", to_simplified(text))


def infer_topic(item, keywords):
    title = to_simplified(item.get("title", ""))
    rhythmic = to_simplified(item.get("rhythmic", ""))

    if title and len(title) <= 10:
        return title

    if rhythmic and len(rhythmic) <= 10:
        return rhythmic

    if keywords:
        return "、".join(keywords[:2])

    return "古风抒怀"


def format_poem(lines):
    """
    把多句诗词合成一个输出字符串。
    """
    lines = [x.strip("，。！？；、 ") for x in lines if x.strip()]
    if not lines:
        return ""

    # 如果是四句，按绝句格式组织
    if len(lines) == 4:
        return f"{lines[0]}，{lines[1]}。{lines[2]}，{lines[3]}。"

    # 宋词或其他长短句
    return "。".join(lines) + "。"


def build_samples():
    random.seed(42)
    samples = []

    for file_path in SELECTED_FILES:
        if not file_path.exists():
            print(f"文件不存在，跳过: {file_path}")
            continue

        source_type = get_source_type(file_path)
        print(f"正在读取: {file_path}，类型: {source_type}")

        try:
            data = json.load(open(file_path, "r", encoding="utf-8"))
        except Exception as e:
            print(f"读取失败: {file_path}, error={e}")
            continue

        if not isinstance(data, list):
            print(f"文件格式不是 list，跳过: {file_path}")
            continue

        for item in data:
            paragraphs = item.get("paragraphs", [])

            lines = split_poem_lines(paragraphs)

            form = detect_form(lines, source_type)
            if form is None:
                continue

            poem = format_poem(lines)
            if not poem:
                continue

            keywords = extract_keywords(poem)
            if not keywords:
                continue
            if sum(len(kw) >= 2 for kw in keywords) < MIN_CONCRETE_KEYWORDS:
                continue

            topic = infer_topic(item, keywords)

            instruction = "请根据主题、关键词和体裁创作一首古诗。"
            input_text = (
                f"主题：{topic}；"
                f"关键词：{'、'.join(keywords)}；"
                f"体裁：{form}"
            )

            samples.append({
                "instruction": instruction,
                "input": input_text,
                "output": poem
            })

    random.shuffle(samples)

    if MAX_SAMPLES is not None:
        samples = samples[:MAX_SAMPLES]

    return samples


def main():
    samples = build_samples()

    OUT_FILE.parent.mkdir(parents=True, exist_ok=True)

    with open(OUT_FILE, "w", encoding="utf-8") as f:
        for s in samples:
            f.write(json.dumps(s, ensure_ascii=False) + "\n")

    print("=" * 50)
    print(f"最终生成样本数: {len(samples)}")
    print(f"保存到: {OUT_FILE}")

    if samples:
        print("样例:")
        print(json.dumps(samples[0], ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
